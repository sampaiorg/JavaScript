// JS é uma linguagem fracamente tipada;
// Uma variável pode ser instanciada inicialmente com um tipo de tipagem e poderá receber outro tipo de tipo durante o processamento;
// Uma Constante não muda o tipo durante o processamento;

let variavel = 'Conteúdo variável';
console.log(variavel);
console.log(typeof variavel);

//agora o conteúdo da variável vai mudar juntamente com o seu tipo;
variavel = 3;
console.log(variavel);
console.log(typeof variavel);

